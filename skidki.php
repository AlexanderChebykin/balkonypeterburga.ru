	<p><strong><em>Толщина профиля</em></strong></p>
		<p>Профиль — это конструкция с внутренними перегородками. Толщина внешних и внутренних стенок профиля нормирована. В зависимости от нее профиль относят к одному из трех классов — А, В или С.</p>
		<p><strong>Класс А</strong> — наружные стенки толщиной от 2,8 мм, внутренние — от 2,5 мм. Окна из такого профиля обеспечивают хорошую тепло- и шумоизоляцию, именно их устанавливают в жилых помещениях.</p>
		<p><strong>Класс В</strong> — наружные стенки толщиной от 2,5 мм, внутренние — от 2 мм. Окна профиля класса В рекомендованы к установке в нежилых помещениях. Они менее долговечны, хуже держат тепло и не обеспечивают достаточной защиты от шума.</p>
		<p><strong>Класс С</strong> — все остальные профили, не подходящие под стандарты А и В. К профилю класса С не предъявляется строгих требований.</p>
		<p>Очевидно, что чем толще профиль, тем он прочнее и лучше. Большинство современных оконных профилей имеют толщину внешних стенок от 3 мм.</p>
	<p><strong><em>Количество воздушных камер</em></strong></p>
		<p>Внутри профиля находится несколько полостей — воздушных камер, обеспечивающих защиту от холода и громких звуков с улицы. Минимальное количество камер в профиле — 3, максимальное — 8. Больше всего распространены трех- и пятикамерные профили. Трехкамерный подойдет для окон, выходящих в тихий двор или на юг, пятикамерные лучше выбрать для окон, выходящих на дорогу или ориентированных на север.</p>
		<p><strong><em>Количество стеклопакетов и их характеристики</em></strong></p>
		<p>Стеклопакетом называют блок из стекол, герметично соединенных между собой. Пространство между двумя листами-стеклами — это камера. Многие путают камеры в профиле и камеры в стеклопакете, но это разные понятия. Самый распространенный стеклопакет — двухкамерный, состоящий из трех стекол и двух камер между ними. Однако встречаются и трех-, и четырехкамерные стеклопакеты для особо холодного климата. Однокамерные пакеты подходят только для остекления лоджий, балконов или беседок.</p>
		<p>Качество стекла имеет не меньшее значение, чем количество камер в стеклопакете. Обычное стекло — самое дешевое, но сегодня многие выбирают стекла с дополнительными свойствами. Например, стекло с покрытием, отражающим инфракрасные лучи. Оно так же прозрачно, как и обыкновенное стекло, но при этом сохраняет тепло зимой и приятную прохладу летом. Бывают огнеупорные стекла, которые не ломаются даже при воздействии экстремально высоких температур, и стекла, ламинированные триплексом. Такое окно практически невозможно разбить, но даже если кому-то это и удастся, при разбивании не будет острых осколков.</p>
	<p><strong><em>Качество уплотнителей</em></strong></p>
		<p>Уплотнители нужны для того, чтобы окно закрывалось плотно, а также для обеспечения герметичности стеклопакетов. Уплотнители делают из резины, резинопластика, силикона или натурального каучука. Самый недорогой (и недолговечный) вариант — резина, в окна подороже устанавливают уплотнители из каучука и силикона. Они не деформируются даже после долгого использования, перепады температур также не влияют на их качество.</p>
		<p><strong><em>Ширина профиля</em></strong></p>
		<p>Ширина профиля должна быть не менее 50 мм. Большинство лучших пластиковых окон имеют ширину 58–70 мм и больше. Толщина в основном зависит от количества стекол: чем их больше, тем окно «теплее» и шире. В большинстве случаев ширина 58–70 мм достаточна для обеспечения тепла и тишины в доме, более широкие профили востребованы в суровом северном климате или же в очень шумных районах.</p>
	<p><strong><em>Фурнитура</em></strong></p>
		<p>Пластиковое окно — довольно тяжелая конструкция, к тому же она постоянно подвергается нагрузкам: окна ежедневно открывают и закрывают. Поэтому фурнитура для таких окон должна быть особенно надежной. Чаще всего владельцы дешевых некачественных окон жалуются именно на фурнитуру: ручки разбалтываются и выходят из строя, а то и вовсе отваливаются, оконные петли заедают и перекашиваются. Чтобы избежать таких неприятностей, покупайте окна солидных производителей, которые не только строго контролируют качество фурнитуры, но и постоянно разрабатывают новые, более удобные и долговечные системы. Регулируемая фурнитура лучше нерегулируемой: она позволяет приоткрывать окно ровно на столько, насколько требуется, а специальные стопоры не позволят сквозняку захлопнуть окно. Противовзломные системы не дадут злоумышленникам проникнуть в дом (это особенно актуально для квартир на первом этаже, дач и частных домов). К достойным производителям оконной фурнитуры можно отнести Roto, Aubi, Winkhaus, Siegenia.</p>

					
